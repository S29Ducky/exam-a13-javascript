function multiplication(a, b) {
    return a * b
}

console.log(multiplication(4, 5)); // Résultat attendu : 20
console.log(multiplication(-1, 15)); // Résultat attendu : -15
console.log(multiplication(0, 1)); // Résultat attendu : 0
console.log(multiplication(-100, 100)); // Résultat attendu : -1000